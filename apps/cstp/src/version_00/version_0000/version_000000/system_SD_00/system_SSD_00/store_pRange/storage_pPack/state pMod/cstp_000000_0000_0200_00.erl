%%              Issuer = processor_ref0
%%              Fault =
%%                {error, [tx]}, Tx format is not available
%% INFO:
%%      Computation is failed because of unavailable arguments
%%
processor_ref0(_,_) ->
    ft(_Issuer={?MODULE, processor_ref0}, _Fault={error, [tx]}).

%%%%%%%%%%%%%%%%%%%%%%%
%%   BOOT Processor  %%
%%%%%%%%%%%%%%%%%%%%%%%
%%
%% processor_ref1({compose, TX}, _)-> Result
%%  TX = binary, recursive composed TX
%%  
%%  Result =
%%      {ok,TX_}
%%          TX_ = binary, recursive composed TX
%% INFO:
%%      Compose CSTX for processor_ref1
%%      A set of tasks to setup storage service
%%
processor_ref1({compose, TX}, _STATE)->
    {ok, <<TX/binary, 1>>};
%%
%% processor_ref1({compute, TX}, _)-> Result
%%  TX = binary, <<  >>
%%  
%%  Result =
%%      {ok,New_state}
%%          New_state = map, the updated STATE
%%      {error, Reason :: term()}
%% INFO:
%%      Compute CSTX
%%      Create or load DB schema
%%      Create or load DB tables
%%      Start and link new crypto_man generic server
%%
processor_ref1({compute, <<>>}, STATE)->
    stopped=mnesia:stop(),
    case mnesia:create_schema([node()]) of
        ok ->
            io:format("create_schema| ~p~n", [ok]),
            %{ok, STATE};
            processor_ref1(db_set_up, STATE);
        {error,R={_NODE,{already_exists,_}}}->
            io:format("create_schema| ~p~n", [{error, R}]),
            %{ok, STATE};
            processor_ref1(db_set_up, STATE);
        {error,Reason}->
            ft(_Issuer={?MODULE, processor_ref1},
                _Fault={error, [create_schema, Reason]})
    end;
processor_ref1(db_set_up, STATE)->
    ok=mnesia:start(),
    ok=processor_ref1(table_set_up, system),
    %ok=processor_ref1(table_set_up, crypto),
    %ok=processor_ref1(table_set_up, sessionID),
    processor_ref2({compute, <<"BOOT">>}, STATE);
processor_ref1(table_set_up, system)->
    case mnesia:force_load_table(system) of
        yes ->
            ok;
        {error,{no_exists,system}}->
            mnesia:create_table(system,
                                [{attributes, record_info(fields, system)},
                                 {type, set},{disc_copies, [node()]}]),
            
            ID =#system{key = id, val = []},
            Crypto =#system{key = crypto, val = #{keys=>[]}},
            TX = fun() ->
                    ok=mnesia:write(ID),
                    ok=mnesia:write(Crypto),
                    ok
                end,
            {atomic, ok} = mnesia:sync_transaction(TX),
            yes=mnesia:force_load_table(system),
            ok
    end;
processor_ref1(table_set_up, crypto)->
    try
        mnesia:table_info(crypto, all),
        yes=mnesia:force_load_table(crypto),
        ok
    catch
        exit:_ ->
            %%  record_info(fields, Record) -> [Field]
            mnesia:create_table(crypto,
                            [{attributes, record_info(fields, crypto)},
                                {type, set},{disc_only, [node()]}]),
            Crypto =
                    #crypto{key = keys,
                            val = []},  %%  list of key objects
            TX = fun() ->
                    mnesia:write(Crypto)
                end,
            mnesia:sync_transaction(TX),
            yes=mnesia:force_load_table(crypto),
            ok
    end;
processor_ref1(table_set_up, sessionID)->
    try
    mnesia:table_info(sessionID, all),
    yes=mnesia:force_load_table(sessionID),
    ok
    catch
        exit:_ ->
            %%  record_info(fields, Record) -> [Field]
            mnesia:create_table(sessionID,
                                [{attributes, record_info(fields, sessionID)},
                                 {type, set},
                                 {disc_copies, [node()]}]),
            yes=mnesia:force_load_table(sessionID),
            ok                     
    end;
